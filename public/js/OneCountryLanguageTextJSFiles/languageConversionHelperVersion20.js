function turkcesiralama(a, b) {
    if (a.length === 0 || b.length === 0) {
        return a.length - b.length;
    }
    for (var i=0; i<a.length; i++) {
        var ai = alfabe.indexOf(a.substring(i,i+1));
        var bi = alfabe.indexOf(b.substring(i,i+1));
        if (ai !== bi) {
            return ai - bi;
        }
    }
}

